public class switches {

    public static void main(String[] args) {

        //switch - a statement that allows a variable to be tested for equality against a list of values

        String cat = "British";

        switch(cat) {
            case "Russian": System.out.println("It is Russian");
            break;
            case "Sphinx": System.out.println("It is Sphinx");
            break;
            case "British": System.out.println("It is British");
            break;
        }

    }
}
